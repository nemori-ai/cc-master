#!/usr/bin/env node
/**
 * Build-time generator: inline @ccm/web-viewer dist into apps/cli/src/generated/web-viewer-assets.ts
 * for SEA packaging (ADR-029 prefer generated asset map).
 */
import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const cliDir = path.resolve(__dirname, '..');
const distDir = path.resolve(cliDir, '../web-viewer/dist');
const outFile = path.resolve(cliDir, 'src/generated/web-viewer-assets.ts');
const pkg = JSON.parse(fs.readFileSync(path.join(cliDir, 'package.json'), 'utf8'));
const version = typeof pkg.version === 'string' ? pkg.version : '0.0.0';

function walk(dir, base = '') {
  /** @type {{ rel: string; abs: string }[]} */
  const entries = [];
  for (const name of fs.readdirSync(dir)) {
    const rel = base ? `${base}/${name}` : name;
    const abs = path.join(dir, name);
    const st = fs.statSync(abs);
    if (st.isDirectory()) entries.push(...walk(abs, rel));
    else entries.push({ rel: rel.replace(/\\/g, '/'), abs });
  }
  return entries;
}

function writeStub(reason) {
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  const body = `// AUTO-GENERATED by scripts/gen-web-viewer-assets.mjs — do not edit manually.
// ${reason}
export const WEB_VIEWER_APP_DIST_BUNDLED = false as const;
export const WEB_VIEWER_APP_DIST_VERSION = ${JSON.stringify(version)};
export const WEB_VIEWER_APP_DIST_FILES: Record<string, string> = {};
`;
  fs.writeFileSync(outFile, body, 'utf8');
  console.log(`[gen-web-viewer-assets] stub (${reason}) → ${path.relative(cliDir, outFile)}`);
}

const indexPath = path.join(distDir, 'index.html');
if (!fs.existsSync(indexPath)) {
  writeStub('web-viewer dist missing; dev uses filesystem candidates or materialize on next build');
  process.exit(0);
}

const files = walk(distDir);
/** @type {Record<string, string>} */
const map = {};
for (const { rel, abs } of files) {
  map[rel] = fs.readFileSync(abs).toString('base64');
}

fs.mkdirSync(path.dirname(outFile), { recursive: true });
const lines = [
  '// AUTO-GENERATED by scripts/gen-web-viewer-assets.mjs — do not edit manually.',
  'export const WEB_VIEWER_APP_DIST_BUNDLED = true as const;',
  `export const WEB_VIEWER_APP_DIST_VERSION = ${JSON.stringify(version)};`,
  'export const WEB_VIEWER_APP_DIST_FILES: Record<string, string> = {',
];
for (const [rel, data] of Object.entries(map).sort(([a], [b]) => a.localeCompare(b))) {
  lines.push(`  ${JSON.stringify(rel)}: ${JSON.stringify(data)},`);
}
lines.push('};', '');
fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
console.log(
  `[gen-web-viewer-assets] bundled ${files.length} files (${Object.keys(map).length} entries) → ${path.relative(cliDir, outFile)}`,
);
