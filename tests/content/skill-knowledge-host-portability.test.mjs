test('SKG-HOST-09: each product host executes a distinct path-token contract, not a relabel', async () => {
  const {
    HOST_ADAPTER_CONTRACT,
    executeHostTokenContract,
  } = await import('../../scripts/skill-knowledge/host-portability/adapter-contract.mjs');
  const { probeHostPayload } = await import(
    '../../scripts/skill-knowledge/host-portability/probe.mjs'
  );

  const fingerprints = new Map();
  for (const host of PRODUCT_HOSTS) {
    const executed = executeHostTokenContract(host);
    assert.equal(executed.ok, true, `${host}: ${JSON.stringify(executed.diagnostics)}`);
    assert.equal(executed.host, host);
    assert.ok(Array.isArray(executed.executed_checks));
    assert.ok(
      executed.executed_checks.includes(`token-contract:${host}`),
      `${host} must execute its own token-contract check`,
    );
    assert.ok(
      executed.evidence.every((item) => item.kind === 'document' || item.kind === 'fixture'),
      `${host} evidence must be document|fixture, never live click-through`,
    );
    assert.equal(
      executed.evidence.some((item) => item.kind === 'live-click-through'),
      false,
    );

    const profile = HOST_ADAPTER_CONTRACT.hosts[host];
    const fingerprint = JSON.stringify({
      host,
      path_tokens: profile.path_tokens,
      executed_checks: executed.executed_checks,
      mechanism: profile.path_tokens.plugin_root_mechanism,
    });
    assert.equal(fingerprints.has(fingerprint), false, 'host profiles must not collapse');
    fingerprints.set(fingerprint, host);

    const payloadRoot = path.join(fixturesRoot, 'payloads', 'canonical');
    const probed = probeHostPayload({ host, mode: 'canonical', payloadRoot, repoRoot });
    assert.equal(probed.ok, true, `${host}/canonical: ${JSON.stringify(probed.diagnostics)}`);
    assert.ok(
      probed.executed_checks.includes(`token-contract:${host}`),
      `${host} probe must run host-specific token contract, not only relabel host=`,
    );
    assert.equal(probed.host_profile.path_tokens.plugin_root_mechanism, profile.path_tokens.plugin_root_mechanism);
  }

  assert.equal(fingerprints.size, PRODUCT_HOSTS.length);

  // Grounding against harness facts (executable, not prose-only).
  const claude = executeHostTokenContract('claude-code');
  assert.equal(claude.path_tokens.plugin_root, '${CLAUDE_PLUGIN_ROOT}');
  assert.equal(claude.path_tokens.skill_dir, '${CLAUDE_SKILL_DIR}');
  assert.equal(claude.path_tokens.plugin_root_expands, true);
  assert.equal(claude.path_tokens.skill_dir_expands, true);

  const codex = executeHostTokenContract('codex');
  assert.equal(codex.path_tokens.plugin_root, '${PLUGIN_ROOT}');
  assert.equal(codex.path_tokens.plugin_root_mechanism, 'hook-command-string-token');
  assert.equal(codex.path_tokens.runtime_env_plugin_root, false);
  assert.ok(codex.path_tokens.rejected_fiction_tokens.includes('${CODEX_PLUGIN_ROOT}'));

  const cursor = executeHostTokenContract('cursor');
  assert.equal(cursor.path_tokens.plugin_root_mechanism, 'launcher-plugin-cwd');
  assert.equal(cursor.path_tokens.plugin_root_expands, false);
  assert.equal(cursor.path_tokens.skill_dir_expands, false);
  assert.ok(cursor.path_tokens.rejected_fiction_tokens.includes('${CURSOR_PLUGIN_ROOT}'));
  assert.ok(cursor.path_tokens.rejected_fiction_tokens.includes('${CURSOR_SKILL_DIR}'));

  const kimi = executeHostTokenContract('kimi-code');
  assert.equal(kimi.path_tokens.skill_dir, '${KIMI_SKILL_DIR}');
  assert.equal(kimi.path_tokens.skill_dir_expands, true);
  assert.equal(kimi.path_tokens.plugin_root_mechanism, 'hook-subprocess-env');
  assert.equal(kimi.path_tokens.plugin_root_expands, false);
});

