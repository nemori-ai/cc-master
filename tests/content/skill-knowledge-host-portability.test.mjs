import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import test from 'node:test';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const fixturesRoot = path.join(
  repoRoot,
  'design_docs',
  'skill-knowledge-graph',
  'fixtures',
  'host-portability',
);
const cliPath = path.join(repoRoot, 'scripts', 'skill-knowledge.mjs');

const PRODUCT_HOSTS = Object.freeze(['claude-code', 'codex', 'cursor', 'kimi-code']);
const WORKER_ALLOWLIST = Object.freeze(['codex', 'cursor']);
const PAYLOAD_MODES = Object.freeze(['canonical', 'partial', 'stub']);

function runCli(args) {
  return spawnSync(process.execPath, [cliPath, ...args], {
    cwd: repoRoot,
    encoding: 'utf8',
  });
}

function withTempDir(run) {
  const root = fs.mkdtempSync(path.join(os.tmpdir(), 'ccm-skg-host-probe-'));
  try {
    return run(root);
  } finally {
    fs.rmSync(root, { recursive: true, force: true });
  }
}

