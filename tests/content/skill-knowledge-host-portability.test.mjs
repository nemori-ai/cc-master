test('SKG-HOST-13: assertHostAdapterContract validates the candidate, not a canonical stamp', async () => {
  const {
    HOST_ADAPTER_CONTRACT,
    assertHostAdapterContract,
  } = await import('../../scripts/skill-knowledge/host-portability/adapter-contract.mjs');

  function clone(value) {
    return JSON.parse(JSON.stringify(value));
  }

  assertHostAdapterContract(HOST_ADAPTER_CONTRACT);
  assertHostAdapterContract(clone(HOST_ADAPTER_CONTRACT));

  // Replace Cursor profile body with Claude profile (keep Cursor worker_eligible).
  const swapped = clone(HOST_ADAPTER_CONTRACT);
  swapped.hosts.cursor = {
    ...clone(HOST_ADAPTER_CONTRACT.hosts['claude-code']),
    worker_eligible: true,
  };
  assert.throws(
    () => assertHostAdapterContract(swapped),
    /cursor|profile|digest|expectation|path.token|canonical/i,
  );

  // Alter Cursor path tokens while attaching a recomputed self-declared fingerprint.
  const altered = clone(HOST_ADAPTER_CONTRACT);
  altered.hosts.cursor.path_tokens = clone(
    HOST_ADAPTER_CONTRACT.hosts['claude-code'].path_tokens,
  );
  altered.hosts.cursor.profile_fingerprint = JSON.stringify({
    host: 'cursor',
    path_tokens: altered.hosts.cursor.path_tokens,
    mechanism: altered.hosts.cursor.path_tokens.plugin_root_mechanism,
  });
  assert.throws(
    () => assertHostAdapterContract(altered),
    /cursor|profile|digest|expectation|path.token|canonical|fingerprint/i,
  );

  // Path-token-only swap must also fail (evidence left alone).
  const tokenSwap = clone(HOST_ADAPTER_CONTRACT);
  tokenSwap.hosts.cursor.path_tokens = clone(
    HOST_ADAPTER_CONTRACT.hosts['claude-code'].path_tokens,
  );
  assert.throws(
    () => assertHostAdapterContract(tokenSwap),
    /cursor|profile|digest|expectation|path.token|canonical/i,
  );
});
