/** Stable digest of path_tokens content — never trust candidate host labels/fingerprints. */
function pathTokenDigest(pathTokens) {
  return JSON.stringify(pathTokens ?? null);
}

/** Expected digests from canonical module profiles (external identity binding). */
const CANONICAL_PATH_TOKEN_DIGESTS = deepFreeze(
  Object.fromEntries(
    PRODUCT_HOSTS.map((host) => [
      host,
      pathTokenDigest(HOST_ADAPTER_CONTRACT.hosts[host].path_tokens),
    ]),
  ),
);

function pathTokensMatchExpectation(actual, expected) {
  if (Array.isArray(expected)) {
    return (
      Array.isArray(actual) &&
      actual.length === expected.length &&
      expected.every((item, index) => actual[index] === item)
    );
  }
  return actual === expected;
}

/**
 * Execute the host-specific path-token / rewrite contract check against the
 * supplied contract (defaults to the frozen module contract).
 * This is intentionally not a host-label stamp over a shared payload.
 */
export function executeHostTokenContract(host, contract = HOST_ADAPTER_CONTRACT) {
  const diagnostics = [];
  if (!PRODUCT_HOSTS.includes(host)) {
    diagnostics.push({
      severity: 'error',
      code: 'SKG-HOST-UNKNOWN',
      message: `Unknown product host "${host}"`,
      location: 'host',
      witness: { host, product_hosts: [...PRODUCT_HOSTS] },
      remediation: 'Use one of claude-code / codex / cursor / kimi-code.',
    });
    return {
      ok: false,
      host,
      path_tokens: null,
      evidence: [],
      executed_checks: [],
      diagnostics,
    };
  }

  const profile = contract?.hosts?.[host];
  if (!profile?.path_tokens) {
    diagnostics.push({
      severity: 'error',
      code: 'SKG-HOST-TOKEN-CONTRACT',
      message: `missing host profile for ${host}`,
      location: `hosts.${host}`,
      witness: { host },
      remediation: 'Provide a distinct path-token profile per product host.',
    });
    return {
      ok: false,
      host,
      path_tokens: null,
      evidence: [],
      executed_checks: [],
      diagnostics,
    };
  }

  const expected = HOST_TOKEN_EXPECTATIONS[host];
  const executedChecks = [
    `token-contract:${host}`,
    `path-rewrite:${profile.path_tokens.plugin_root_mechanism}`,
  ];

  for (const [key, value] of Object.entries(expected)) {
    const actual = profile.path_tokens[key];
    if (!pathTokensMatchExpectation(actual, value)) {
      diagnostics.push({
        severity: 'error',
        code: 'SKG-HOST-TOKEN-CONTRACT',
        message: `${host} path token fact "${key}" drifted from harness-grounded expectation`,
        location: `hosts.${host}.path_tokens.${key}`,
        witness: { host, key, actual, expected: value },
        remediation: 'Align HOST_ADAPTER_CONTRACT with design_docs/harnesses path-token facts.',
      });
    }
  }

  const actualDigest = pathTokenDigest(profile.path_tokens);
  const expectedDigest = CANONICAL_PATH_TOKEN_DIGESTS[host];
  if (actualDigest !== expectedDigest) {
    diagnostics.push({
      severity: 'error',
      code: 'SKG-HOST-TOKEN-CONTRACT',
      message: `${host} profile path_tokens digest does not match canonical harness-grounded digest`,
      location: `hosts.${host}.path_tokens`,
      witness: {
        host,
        actual_digest: actualDigest,
        expected_digest: expectedDigest,
      },
      remediation:
        'Bind each host to its canonical path-token profile; do not relabel or swap profiles.',
    });
  }

  if (profile.path_tokens.plugin_root === '${CODEX_PLUGIN_ROOT}') {
    diagnostics.push({
      severity: 'error',
      code: 'SKG-HOST-TOKEN-CONTRACT',
      message: 'Codex must not freeze fictitious ${CODEX_PLUGIN_ROOT} as plugin_root',
      location: 'hosts.codex.path_tokens.plugin_root',
      witness: { plugin_root: profile.path_tokens.plugin_root },
      remediation: 'Use ${PLUGIN_ROOT} hook command token; reject ${CODEX_PLUGIN_ROOT} as fiction.',
    });
  }

  try {
    assertEvidenceList(profile.evidence, host);
  } catch (error) {
    diagnostics.push({
      severity: 'error',
      code: 'SKG-HOST-TOKEN-CONTRACT',
      message: error.message,
      location: `hosts.${host}.evidence`,
      witness: { host, evidence: profile.evidence },
      remediation: 'Label evidence as document or fixture only.',
    });
  }

  return {
    ok: diagnostics.length === 0,
    host,
    path_tokens: profile.path_tokens,
    evidence: profile.evidence,
    executed_checks: executedChecks,
    diagnostics,
  };
}

export function assertHostAdapterContract(contract = HOST_ADAPTER_CONTRACT) {
  if (!contract || typeof contract !== 'object') {
    throw new Error('host adapter contract missing');
  }
  sameStringList(contract.product_hosts, PRODUCT_HOSTS, 'product_hosts');
  sameStringList(contract.payload_modes, PAYLOAD_MODES, 'payload_modes');
  if (contract.anchor_form !== ANCHOR_FORM) {
    throw new Error(`anchor_form must be ${ANCHOR_FORM}`);
  }
  if (contract.path_policy !== PATH_POLICY) {
    throw new Error(`path_policy must be ${PATH_POLICY}`);
  }

  if (
    !Array.isArray(contract.worker_allowlist) ||
    contract.worker_allowlist.length !== WORKER_ALLOWLIST.length
  ) {
    throw new Error(
      'worker allowlist must stay Codex/Cursor-only; do not collapse the product-host bounded context',
    );
  }
  for (const host of contract.worker_allowlist) {
    if (!WORKER_ALLOWLIST.includes(host)) {
      throw new Error(
        `worker allowlist must stay Codex/Cursor-only; unexpected host ${host}`,
      );
    }
    if (!contract.product_hosts.includes(host)) {
      throw new Error(`worker allowlist host ${host} is outside product hosts`);
    }
  }
  sameStringList(contract.worker_allowlist, WORKER_ALLOWLIST, 'worker_allowlist');

  // Digests of path_tokens only — ignore any candidate self-declared fingerprint.
  const digests = new Set();
  for (const host of PRODUCT_HOSTS) {
    const profile = contract.hosts?.[host];
    if (!profile) throw new Error(`missing host profile for ${host}`);
    if (profile.role !== 'product-host') {
      throw new Error(`${host} role must be product-host`);
    }
    const expectedWorker = WORKER_ALLOWLIST.includes(host);
    if (profile.worker_eligible !== expectedWorker) {
      throw new Error(
        `${host} worker_eligible must be ${expectedWorker} (bounded context)`,
      );
    }
    assertEvidenceList(profile.evidence, host);

    // Validate the actual candidate argument (not a side-channel canonical stamp).
    const executed = executeHostTokenContract(host, contract);
    if (!executed.ok) {
      throw new Error(
        `${host} token contract failed: ${executed.diagnostics.map((item) => item.message).join('; ')}`,
      );
    }

    const digest = pathTokenDigest(profile.path_tokens);
    if (digest !== CANONICAL_PATH_TOKEN_DIGESTS[host]) {
      throw new Error(
        `${host} profile path_tokens must match canonical harness-grounded digest`,
      );
    }
    if (digests.has(digest)) {
      throw new Error('host profiles must stay distinct; relabeling one payload is forbidden');
    }
    digests.add(digest);
  }

  if (contract.hosts.codex.path_tokens.plugin_root !== '${PLUGIN_ROOT}') {
    throw new Error('codex plugin_root must be ${PLUGIN_ROOT}');
  }
  if (
    !contract.hosts.codex.path_tokens.rejected_fiction_tokens.includes('${CODEX_PLUGIN_ROOT}')
  ) {
    throw new Error('codex must reject fictitious ${CODEX_PLUGIN_ROOT}');
  }

  const c9 = HARDENING_CONTRACT.C9;
  sameStringList(c9.hosts, PRODUCT_HOSTS, 'hardening_contract.C9.hosts');
  sameStringList(
    c9.worker_allowlist,
    WORKER_ALLOWLIST,
    'hardening_contract.C9.worker_allowlist',
  );
  sameStringList(c9.payload_modes, PAYLOAD_MODES, 'hardening_contract.C9.payload_modes');
  if (c9.anchor_form !== ANCHOR_FORM || c9.path_policy !== PATH_POLICY) {
    throw new Error('hardening_contract.C9 drifted from host adapter contract');
  }

  return true;
}

