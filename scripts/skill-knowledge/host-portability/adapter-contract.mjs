function sameStringList(actual, expected, label) {
  if (!Array.isArray(actual) || actual.length !== expected.length) {
    throw new Error(`${label} must equal [${expected.join(', ')}]`);
  }
  for (let index = 0; index < expected.length; index += 1) {
    if (actual[index] !== expected[index]) {
      throw new Error(`${label} must equal [${expected.join(', ')}]`);
    }
  }
}

function assertEvidenceList(evidenceList, host) {
  if (!Array.isArray(evidenceList) || evidenceList.length === 0) {
    throw new Error(`${host} evidence must be a non-empty document|fixture list`);
  }
  for (const item of evidenceList) {
    if (!item || (item.kind !== 'document' && item.kind !== 'fixture')) {
      throw new Error(
        `${host} evidence must be labeled document|fixture (never live click-through)`,
      );
    }
    if (typeof item.path !== 'string' || item.path.length === 0) {
      throw new Error(`${host} evidence path missing`);
    }
  }
}

